package chapter02_2;

public class 연산자_비교연산자_2 {

	public static void main(String[] args) {
		double d = 30.5;
		double e = 20.0;
		
		System.out.println("d = "+d+ " e = " + e);
		System.out.println(d+" == " + e + ": " + (d==e));
		System.out.println(d+" != " + e + ": " + (d!=e));
		System.out.println(d+" < " + e + ": " + (d<e));
		System.out.println(d+" > " + e + ": " + (d>e));
		System.out.println(d+" <= " + e + ": " + (d<=e));
		System.out.println(d+" >= " + e + ": " + (d>=e));
		

	}

}
